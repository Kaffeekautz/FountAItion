interface StatuteVariables {
  associationName: string;
  associationSeat: string;
  associationPurpose: string;
}

export function buildStatutePreview({ associationName, associationSeat, associationPurpose }: StatuteVariables) {
  return statuteTemplate
    .split("[VEREINSNAME]")
    .join(associationName || "[VEREINSNAME]")
    .split("[VEREINSSITZ]")
    .join(associationSeat || "[VEREINSSITZ]")
    .split("{{VEREINSSITZ_FOLLOWUP}}")
    .join(associationSeat || "(ORTSANGABE ENTSPRECHEND § 1 ABSATZ 3)")
    .split("[VEREINSZWECK]")
    .join(associationPurpose || "[VEREINSZWECK]");
}
